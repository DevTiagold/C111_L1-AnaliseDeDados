#Conjuntos --> Diferente das tuplas e listas, usa-se {}
nomes = {'Goku', 'Vegeta', 'Trunks', 'Gohan', 'Trunks', 'Goku'}
print(nomes)

#Características dos conjuntos:
#1º Não possui elementos repetidos
#2º Não guarda a ordem, ISSO NÃO É UMA PROPRIEDADE NEGATIVA, existem aplicações  "mosntruosas" para conjuntos
#3º Parcialmente mutáveis

# ADICIONAR
nomes.add('Kami')

# DELETE
nomes.remove('Vegeta')

